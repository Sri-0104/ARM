#include "IOPIN_usage.h"
#define LED 1<<17
unsigned char bat[]={0x0e,0x0a,0x1b,0x11,0x11,0x11,0x1f,0x00,
										 0x0e,0x0a,0x1b,0x11,0x11,0x1f,0x1f,0x00,
										 0x0e,0x0a,0x1b,0x11,0x1f,0x1f,0x1f,0x00,
										 0x0e,0x0a,0x1b,0x1f,0x1f,0x1f,0x1f,0x00,
										 0x0e,0x0a,0x1f,0x1f,0x1f,0x1f,0x1f,0x00,
										 0x0e,0x0e,0x1f,0x1f,0x1f,0x1f,0x1f,0x00
										};

void cgram_write(void);
int main()
{
	//char s;
	//IODIR0|=LED;
	LCD_init();
	IODIR0|=LED;
	cgram_write();
	IOSET0 = LED;
	
	while(1)
	{
		char s;
		//LCD_cmd(0x80);
		for(s=0;s<6;s++)
		{
			LCD_cmd(0x80);
			LCD_data(s);
			delayms(300);
		}		
	}
}

void cgram_write()
{
	char i;
	LCD_cmd(0x40);
	for(i=0;i<48;i++)
	LCD_data(bat[i]);
}

