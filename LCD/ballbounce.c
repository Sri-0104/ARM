void cgram_write(void);
int main()
{
	
	LCD_init();
	cgram_write();
while(1)
{
	char s,bounce=2;
	while(bounce!=0)
	{
		//char s;
		//LCD_cmd(0x80);
		for(s=0;s<6;s++)
		{
			LCD_cmd(0x80);
			LCD_data(s);
			delayms(300);
		}		bounce--;
	}
	for(s=0;s<15;s++)
	{
		if(s%2!=0)
		{
			LCD_data(0);
			delayms(300);
			LCD_cmd(0x81+s);
			LCD_data(5);
		}
		else
		{
			LCD_data(2);
			delayms(300);
			LCD_cmd(0x81+s);
			LCD_data(5);
		}
	}delayms(200);
}
}

void cgram_write()
{
	char i;
	LCD_cmd(0x40);
	for(i=0;i<48;i++)
	LCD_data(bat[i]);
}

